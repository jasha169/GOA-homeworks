# სტრინგზე უშუალოდ გამოძახებული მეთოდები
text = "გამარჯობა მსოფლიო"

print(text.lower())       # ყველა სიმბოლო პატარა ასოებად → 'გამარჯობა მსოფლიო'
print(text.upper())       # ყველა სიმბოლო დიდი ასოებად → 'გამარჯობა მსოფლიო'
print(text.capitalize())  # მხოლოდ პირველი სიმბოლო დიდი → 'გამარჯობა მსოფლიო'
s = "Hello Python World"

# 1. წარმატებული მოძებნა – დაბრუნდება ინდექსი
print(s.find("Python"))     # ✅ შედეგი: 6

# 2. არაპირდაპირი შეცდომა – ვერ მოიძებნა
print(s.find("Java"))       # ❌ შედეგი: -1

# 3. ცდომილება, თუ find-ს არგუმენტი არ მისცეს ან გამოყენება არასწორია
try:
    print(s.find())         # ❌ Error, არგუმენტი არ მიეცა
except TypeError as e:
    print("მოხდა შეცდომა:", e)
