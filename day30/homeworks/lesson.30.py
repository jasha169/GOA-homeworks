from turtle import *

def draw_square_at(x, y, size=100):
    """
    ხატავს კვადრატს, რომელიც იწყება კოორდინატებიდან (x, y)
    size - კვადრატის გვერდის სიგრძე (სტანდარტულად 100)
    """
    penup()            # აწეული კალამი, რომ გადავიტანოთ
    goto(x, y)         # გადავიტანოთ კოორდინატებზე
    pendown()          # კალამი დაწერილ მდგომარეობაში

    for _ in range(4):
        forward(size)  # წავალთ წინ
        right(90)      # გადავუხვიოთ მარჯვნივ 90 გრადუსით

# ფანჯრის გახსნა და ფუნქციის გამოძახება
if __name__ == "__main__":
    speed(1)            # ნახატის სწრაფი შენელება (1 - ნელი, 10 - სწრაფი)
    draw_square_at(0, 0)  # კვადრატის დახატვა კოორდინატებზე (0,0)
    done()              # ფანჯრის დახურვამდე პროგრამის გამორთვა
