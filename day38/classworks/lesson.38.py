# .isnumeric()
text = "12345"
print(text.isnumeric())  # True

print("123abc".isnumeric())  # False

# sorted()
values = [9, 2, 5]
new_list = sorted(values)
print(new_list)  # [2, 5, 9]

print(sorted([10, 3, 7]))  # [3, 7, 10]
 
#  min()
nums = [8, 1, 6]
print(min(nums))  # 1

print(min(5, 3, 9))  # 3


# max()
scores = [12, 98, 34]
print(max(scores))  # 98

print(max(5, 17, 4))  # 17

 
