# 1. рЃЋрЃЦрЃЏрЃюрЃўрЃЌ рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃўрЃА set-рЃА
numbers = {1, 2, 3, 4, 5}

# 2. рЃЋрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃЌ рЃЏрЃўрЃАрЃў рЃФрЃўрЃарЃўрЃЌрЃљрЃЊрЃў рЃЌрЃЋрЃўрЃАрЃћрЃЉрЃћрЃЉрЃўрЃА рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃћрЃЉрЃА
print("РъА№ИЈ рЃАрЃљрЃгрЃДрЃўрЃАрЃў set:", numbers)

# рЃЌрЃЋрЃўрЃАрЃћрЃЉрЃћрЃЉрЃў:
# - set рЃљрЃа рЃўрЃюрЃљрЃ«рЃљрЃЋрЃА рЃЊрЃБрЃЉрЃџрЃўрЃарЃћрЃЉрЃБрЃџ рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЉрЃћрЃЉрЃА
# - рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃўрЃА рЃњрЃљрЃюрЃџрЃљрЃњрЃћрЃЉрЃљ рЃљрЃа рЃљрЃарЃўрЃА рЃњрЃљрЃарЃљрЃюрЃбрЃўрЃарЃћрЃЉрЃБрЃџрЃў (рЃљрЃа рЃљрЃарЃўрЃА рЃЏрЃЮрЃгрЃћрЃАрЃарЃўрЃњрЃћрЃЉрЃБрЃџрЃў)
# - set-рЃўрЃА рЃЌрЃўрЃЌрЃЮрЃћрЃБрЃџрЃў рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃў рЃБрЃюрЃўрЃЎрЃљрЃџрЃБрЃарЃўрЃљ
# - set рЃерЃћрЃўрЃфрЃљрЃЋрЃА рЃфрЃЋрЃџрЃљрЃЊ рЃбрЃўрЃърЃўрЃА рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃА (рЃЏрЃљрЃњ. рЃЏрЃЌрЃћрЃџрЃў рЃарЃўрЃфрЃ«рЃЋрЃћрЃЉрЃў, рЃАрЃбрЃарЃўрЃюрЃњрЃћрЃЉрЃў, рЃЏрЃљрЃњрЃарЃљрЃЏ рЃљрЃарЃљ рЃфрЃЋрЃџрЃљрЃЊрЃў рЃбрЃўрЃърЃћрЃЉрЃў, рЃарЃЮрЃњрЃЮрЃарЃўрЃфрЃљрЃљ рЃАрЃўрЃћрЃЉрЃў)

# 3. рЃЋрЃљрЃЏрЃљрЃбрЃћрЃЉрЃЌ 2 рЃљрЃ«рЃљрЃџ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃА set-рЃерЃў
numbers.add(6)
numbers.add(7)
print("РъЋ рЃЊрЃљрЃЏрЃљрЃбрЃћрЃЉрЃБрЃџрЃў рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃў (6 рЃЊрЃљ 7):", numbers)

# 4. рЃЋрЃерЃџрЃўрЃЌ 2 рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃА set-рЃўрЃЊрЃљрЃю
numbers.remove(1)
numbers.remove(3)
print("Ръќ рЃгрЃљрЃерЃџрЃўрЃџрЃў рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃў (1 рЃЊрЃљ 3):", numbers)

# 5. рЃЋрЃЦрЃЏрЃюрЃўрЃЌ рЃЏрЃћрЃЮрЃарЃћ set-рЃА
more_numbers = {4, 5, 6, 8, 9}
print("РъА№ИЈ рЃЏрЃћрЃЮрЃарЃћ set:", more_numbers)

# 6. рЃЋрЃљрЃАрЃарЃБрЃџрЃћрЃЉрЃЌ рЃАрЃљрЃЏ рЃЏрЃЮрЃЦрЃЏрЃћрЃЊрЃћрЃЉрЃљрЃА рЃЮрЃа set-рЃА рЃерЃЮрЃарЃўрЃА

# UNION РђЊ рЃљрЃћрЃарЃЌрЃўрЃљрЃюрЃћрЃЉрЃА рЃЮрЃарЃўрЃЋрЃћ set-рЃўрЃА рЃДрЃЋрЃћрЃџрЃљ рЃБрЃюрЃўрЃЎрЃљрЃџрЃБрЃа рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃА
union_set = numbers.union(more_numbers)
print("­ЪћЌ Union (рЃњрЃљрЃћрЃарЃЌрЃўрЃљрЃюрЃћрЃЉрЃљ):", union_set)

# INTERSECTION РђЊ рЃърЃЮрЃБрЃџрЃЮрЃЉрЃА рЃАрЃљрЃћрЃарЃЌрЃЮ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃА
intersection_set = numbers.intersection(more_numbers)
print("­Ъј» Intersection (рЃњрЃљрЃЊрЃљрЃЎрЃЋрЃћрЃЌрЃљ):", intersection_set)

# DIFFERENCE РђЊ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃА, рЃарЃЮрЃЏрЃџрЃћрЃЉрЃўрЃф рЃЏрЃ«рЃЮрЃџрЃЮрЃЊ рЃърЃўрЃарЃЋрЃћрЃџрЃерЃўрЃљ рЃЊрЃљ рЃљрЃарЃљ рЃЏрЃћрЃЮрЃарЃћрЃерЃў
difference_set = numbers.difference(more_numbers)
print("­ЪД« Difference (рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃљ):", difference_set)
